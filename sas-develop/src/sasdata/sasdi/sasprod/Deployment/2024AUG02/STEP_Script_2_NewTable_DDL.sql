CREATE TABLE EDRTLRP1D.PIT_STATUS_PRE_STEP (  
MTH_TM_ID INTEGER NOT NULL
,SRC_SYS_CD CHAR(10) NOT NULL
,BASEL_ACCT_ID BIGINT NOT NULL
,MORT_NUM INTEGER
,MORT_PROCESS_DATE TIMESTAMP
,PIT_STATUS_PRE_STEP CHAR(10)
,STEP_PLN_SNAPSHOT_ID BIGINT NOT NULL
,STEP_PLN_AGRMNT_NUM CHAR(23)
,STEP_DFLT_F CHAR(1)
,CROSS_DFLT_PIT_OVERRIDE_F CHAR(1)
